class PMCComponent {

    public PMCComponent(String name, String description, String initialValue, String type, int[] numberOfElements) {
        this.name = name;
        this.description = description;
        this.initialValue = initialValue;
        this.type = type;
        this.numberOfElements = numberOfElements;
    }

    public String name;
    public String description;
    public String initialValue;
    public String type;
    public int[] numberOfElements;
            
}

